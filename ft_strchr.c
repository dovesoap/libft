#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*ret;

	i = 0;
	while (s[i - 1] != '\0' || i == 0)
	{
		if (s[i] == c)
		{
			ret = (char *)s;
			return (&ret[i]);
		}
		i++;
	}
	return (NULL);
}
